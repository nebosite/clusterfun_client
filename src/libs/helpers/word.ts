const templates = [
    "~noun ~tverb ~noun",
    "~adjective ~noun is ~adjective",
    "~noun ~noun",
    "~adverb ~adjective ~noun"
]

//--------------------------------------------------------------------------------------
// Generate a random english-like sentence.
//--------------------------------------------------------------------------------------
export const generateSentance = (template: string = rand.pick(templates)) => {
    let output = "";
    for(let part of template.split(" ")) {
        if(part === "") continue;
        if(part[0] === "~") {
            part = part.substring(1).toLowerCase();
            let array = [part];
            switch(part) {
                case 'noun': array = nouns; break;
                case 'adjective': array = adjectives; break;
                case 'adverb': array = adverbs; break;
                case 'tverb': array = verbs_transitive; break;
                case 'varb': array = verbs; break;
            }
            if(output.length > 0) output += " ";
            output += rand.pick(array);
        }
    }
    return output;
}

